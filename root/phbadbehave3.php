		if (0 == (int)$db->sql_fetchfield('count') % 10)
		{
			if (-1 != (int)$settings['keep_days'])
			{
				$db->sql_query(
					'DELETE 
					FROM ' .BAD_BEHAVIOR_TABLE . ' 
