			if (-1 != (int) $settings['keep_amount'])
			{
				$db->sql_query(
					'SELECT MAX(id) AS max 
					FROM ' . BAD_BEHAVIOR_TABLE);
				$num = (int)$db->sql_fetchfield('max') - (int) $settings['keep_amount'];
				$db->sql_query(
					'DELETE 
					FROM ' . BAD_BEHAVIOR_TABLE . ' 
					WHERE id < ' . $num);
			}
		}
	}
